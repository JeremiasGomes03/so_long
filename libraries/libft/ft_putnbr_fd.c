#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	number;

	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	else
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		number = n % 10 + '0';
		write(fd, &number, 1);
	}
}
