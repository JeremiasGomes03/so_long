#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*copy;
	int		size;

	size = 0;
	while (src[size])
		size++;
	copy = malloc(sizeof(char) * (size + 1));
	if (!copy)
		return (NULL);
	i = 0;
	while (src[i])
	{
		copy[i] = src[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
